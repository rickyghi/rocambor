export function cardDisplayLabel(card: Card, locale: Locale): string {
  const rank = RANK_LABELS[card.r]?.[locale] ?? String(card.r);
  return locale === "es"
    ? `${rank} de ${suitLabel(card.s, locale)}`
    : `${rank} of ${suitLabel(card.s, locale)}`;
}

export function formatPlacement(place: number, locale: Locale): string {
  if (locale === "es") return `${place}.º`;
  const remainder = place % 10;
  const teen = place % 100;
  if (teen >= 11 && teen <= 13) return `${place}th`;
  if (remainder === 1) return `${place}st`;
  if (remainder === 2) return `${place}nd`;
  if (remainder === 3) return `${place}rd`;
  return `${place}th`;
}

export function formatMemberSince(value: string, locale: Locale): string {
  const { t } = createTranslator(locale);
  const date = new Date(value);
  if (Number.isNaN(date.getTime())) return t("profile.memberSinceFallback");
  return t("profile.memberSince", {
    date: new Intl.DateTimeFormat(localeTag(locale), {
      month: "short",
      year: "numeric",
    }).format(date),
  });
}

export function formatRelativeTime(value: string, locale: Locale): string {
  const { t } = createTranslator(locale);
  const date = new Date(value);
  if (Number.isNaN(date.getTime())) return t("profile.justNow");
  const diffMs = Date.now() - date.getTime();
  const diffHours = Math.max(0, Math.round(diffMs / (1000 * 60 * 60)));
  if (diffHours < 1) return t("profile.justNow");
  if (diffHours < 24) {
    return t("profile.hoursAgo", {
      count: diffHours,
      unit: diffHours === 1 ? t("profile.hour") : t("profile.hours"),
    });
  }
  const diffDays = Math.round(diffHours / 24);
  if (diffDays < 7) {
    return t("profile.daysAgo", {
      count: diffDays,
      unit: diffDays === 1 ? t("profile.day") : t("profile.days"),
    });
  }
  return new Intl.DateTimeFormat(localeTag(locale), {
    month: "short",
    day: "numeric",
  }).format(date);
}
