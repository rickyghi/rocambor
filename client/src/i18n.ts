function interpolate(message: string, params?: Params): string {
  if (!params) return message;
  return message.replace(/\{(\w+)\}/g, (_, key: string) => {
    const value = params[key];
    return value === undefined ? `{${key}}` : String(value);
  });
}

export function localeTag(locale: Locale): string {
  return locale === "es" ? "es-ES" : "en-US";
}

export function createTranslator(locale: Locale) {
  return {
    locale,
    t(key: string, params?: Params): string {
      const message = MESSAGES[locale][key] ?? MESSAGES.en[key] ?? key;
      return interpolate(message, params);
    },
  };
}

export function modeLabel(mode: "tresillo" | "quadrille", locale: Locale, compact = false): string {
  if (mode === "tresillo") return compact ? "Tresillo (3P)" : "Tresillo";
  return compact ? "Quadrille (4P)" : locale === "es" ? "Quadrille" : "Quadrille";
}

