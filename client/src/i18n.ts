export function contractDisplayLabel(
  contract: string | null,
  trump: string | null,
  locale: Locale
): string {
  if (!contract) return "";
  const base = CONTRACT_LABELS[contract]?.[locale] ?? contract;
  if (trump && !["oros", "solo_oros", "contrabola", "bola"].includes(contract)) {
    return `${base} ${suitLabel(trump, locale)}`;
  }
  return base;
}

